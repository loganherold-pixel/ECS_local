let referenceIndexByMake: Map<string, readonly FleetOemVehicleSpecReference[]> | null = null;

function getReferenceIndexByMake(): Map<string, readonly FleetOemVehicleSpecReference[]> {
  if (referenceIndexByMake) return referenceIndexByMake;
  const index = new Map<string, FleetOemVehicleSpecReference[]>();
  for (const reference of FLEET_OEM_SPEC_REFERENCES) {
    const make = normalizeVehicleText(reference.make);
    const existing = index.get(make) ?? [];
    existing.push(reference);
    index.set(make, existing);
  }
  referenceIndexByMake = index;
  return referenceIndexByMake;
}

export function getFleetOemSpecReferenceCatalogStats(): {
  referenceCount: number;
  makeCount: number;
  vehicleTypes: string[];
  earliestYearStart: number;
  latestYearEnd: number;
} {
  const years = FLEET_OEM_SPEC_REFERENCES.flatMap((reference) => [reference.yearStart, reference.yearEnd ?? currentModelYear]);
  return {
    referenceCount: FLEET_OEM_SPEC_REFERENCES.length,
    makeCount: new Set(FLEET_OEM_SPEC_REFERENCES.map((reference) => normalizeVehicleText(reference.make))).size,
    vehicleTypes: Array.from(new Set(FLEET_OEM_SPEC_REFERENCES.map((reference) => normalizeVehicleText(reference.vehicleType)))).sort(),
    earliestYearStart: Math.min(...years),
    latestYearEnd: Math.max(...years),
  };
}

export function resolveFleetOemSpecReference(input: {
  year?: number | null;
  make?: string | null;
  model?: string | null;
  trim?: string | null;
  vehicleType?: string | null;
}): FleetOemSpecMatch {
  const make = normalizeVehicleText(input.make);
  const model = normalizeVehicleText(input.model);
  const trim = normalizeVehicleText(input.trim);
  const vehicleType = normalizeVehicleText(input.vehicleType);
  const year = typeof input.year === 'number' && Number.isFinite(input.year) ? input.year : null;
  if (!make || !model) {
    return {
      status: 'incomplete',
      reference: null,
      message: 'Enter year, make, and model for ECS to search OEM reference specs.',
    };
  }

  const search = normalizeVehicleText([input.make, input.model, input.trim, input.vehicleType].filter(Boolean).join(' '));
  const indexedReferences = getReferenceIndexByMake().get(make) ?? FLEET_OEM_SPEC_REFERENCES;
  const candidates = indexedReferences
    .filter((reference) => makeMatches(make, reference.make))
    .filter((reference) => modelMatches(model, reference.model, search));

  if (candidates.length === 0) {
    return {
      status: 'not_found',
      reference: null,
      message: 'No OEM reference is bundled for this vehicle yet. ECS will use class/model estimates until specs are confirmed.',
    };
  }

  const inYear = candidates.filter((reference) => yearInRange(year, reference));
  if (inYear.length === 0) {
    const range = candidates
      .map((reference) => `${reference.yearStart}${reference.yearEnd ? `-${reference.yearEnd}` : '+'}`)
      .join(', ');
    return {
      status: 'unsupported_year',
      reference: null,
      message: `This model/year combination is outside the bundled OEM reference window (${range}). Verify the year or enter specs manually.`,
    };
  }

  const scored = inYear
    .map((reference) => {
      let score = 10;
      const referenceModel = normalizeVehicleText(reference.model);
      if (model === referenceModel) score += 12;
      else if (model.includes(referenceModel)) score += Math.min(8, referenceModel.length);
      if (reference.trim && trim && trim.includes(normalizeVehicleText(reference.trim))) score += 8;
      if (reference.vehicleType && vehicleType && vehicleType.includes(normalizeVehicleText(reference.vehicleType))) score += 3;
      return { reference, score };
    })
    .sort((a, b) => b.score - a.score);

  const reference = scored[0]?.reference;
  if (!reference) {
    return {
      status: 'not_found',
      reference: null,
      message: 'No OEM reference is bundled for this vehicle yet. ECS will use class/model estimates until specs are confirmed.',
    };
  }

  return {
    status: 'matched',
    reference,
    message: `${reference.label} matched. Specs are OEM-reference estimates and should be verified against trim/package and door placard before final payload decisions.`,
  };
}
