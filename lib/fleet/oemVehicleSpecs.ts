export type FleetOemSpecMatch =
  | {
      status: 'matched';
      reference: FleetOemVehicleSpecReference;
      message: string;
    }
  | {
      status: 'unsupported_year' | 'not_found' | 'incomplete';
      reference: null;
      message: string;
    };

function normalizeVehicleText(value: string | number | null | undefined): string {
  return String(value ?? '')
    .toLowerCase()
    .replace(/&/g, ' and ')
    .replace(/[^a-z0-9]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function makeMatches(inputMake: string, referenceMake: string): boolean {
  if (inputMake === referenceMake || inputMake.includes(referenceMake) || referenceMake.includes(inputMake)) return true;
  const chevyInput = inputMake === 'chevy' || inputMake === 'chevrolet';
  const chevyRef = referenceMake === 'chevy' || referenceMake === 'chevrolet';
  const mercedesInput = inputMake === 'mercedes' || inputMake === 'mercedes benz';
  const mercedesRef = referenceMake === 'mercedes' || referenceMake === 'mercedes benz';
  return (chevyInput && chevyRef) || (mercedesInput && mercedesRef);
}

function modelMatches(inputModel: string, referenceModel: string, search: string): boolean {
  if (inputModel === referenceModel) return true;
  if (inputModel.includes(referenceModel) || referenceModel.includes(inputModel)) return true;
  return search.includes(referenceModel);
}

function yearInRange(year: number | null, reference: FleetOemVehicleSpecReference): boolean {
  if (year == null) return true;
  if (year < reference.yearStart) return false;
  if (reference.yearEnd != null && year > reference.yearEnd) return false;
  return true;
}

